package com.maro.client.common.constant.enumconstant;

import com.maro.client.common.annotation.EnumSqlScript;

/**
 * 座位类型的枚举
 * @author 冯成果
 * @date 2018-3-26
 * @since 01.00.0001
 */
@EnumSqlScript(group = "seatTypeEnum",groupName = "座位类型")
public enum SeatTypeEnum {

    TWO(2, "两人桌"),
    FOUR(4, "四人桌"),
    EIGHT(8, "八人桌"),
    TEN(10, "十人桌"),
    FIFTEEN(15, "十五人桌"),
    TWENTY(20, "二十人桌");




    private String name;
    private Integer code;

    public String getName() {
        return name;
    }

    public Integer getCode() {
        return code;
    }
    SeatTypeEnum(Integer code, String name){
        this.code = code;
        this.name = name;
    }

}
